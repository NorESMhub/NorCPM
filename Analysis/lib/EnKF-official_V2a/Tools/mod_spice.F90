module mod_spice

contains

   real function spice(t,s)

   implicit none

   real, intent(in)  :: t,s
   real :: sp,t2,s2,s3
   integer :: i,j
   real*8 :: b(6,5)



   b(1,1) = 0
   b(1,2) = 7.7442e-001
   b(1,3) = -5.85e-003
   b(1,4) = -9.84e-004
   b(1,5) = -2.06e-004

   b(2,1) = 5.1655e-002
   b(2,2) = 2.034e-003
   b(2,3) = -2.742e-004
   b(2,4) = -8.5e-006
   b(2,5) = 1.36e-005

   b(3,1) = 6.64783e-003
   b(3,2) = -2.4681e-004
   b(3,3) = -1.428e-005
   b(3,4) = 3.337e-005
   b(3,5) = 7.894e-006

   b(4,1) = -5.4023e-005
   b(4,2) = 7.326e-006
   b(4,3) = 7.0036e-006
   b(4,4) = -3.0412e-006
   b(4,5) = -1.0853e-006

   b(5,1) = 3.949e-007
   b(5,2) = -3.029e-008
   b(5,3) = -3.8209e-007
   b(5,4) = 1.0012e-007
   b(5,5) = 4.7133e-008

   b(6,1) = -6.36e-010
   b(6,2) = -1.309e-009
   b(6,3) = 6.048e-009
   b(6,4) = -1.1409e-009
   b(6,5) = -6.676e-010

   sp=0.
   t2=1.
   s3=s-35.
   do i=1,6
      s2=1.
      do j=1,5
         sp=sp+b(i,j)*t2*s2
         s2=s2*s3
      end do
      t2=t2*t
   end do
   spice=sp
   end function spice


end module mod_spice

